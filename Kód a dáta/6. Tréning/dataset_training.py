# Definícia ciest pre kód
TRAIN_CSV = 'data_train.csv'
VAL_CSV   = 'data_val.csv'
TEST_CSV  = 'data_test.csv'
IMG_DIR   = 'sentinel_data'

TABULAR_COLUMNS = [
    'ndvi', 'ndwi', 'ndmi', 'gndvi', 'reci', 'red', 'nir', 
    'temp_mean', 'precip_sum', 'wind_mean'                  
]

class VoleDataset(Dataset):
    def __init__(self, csv_file, img_dir, tabular_cols, transform=None, scaler=None):
        self.df = pd.read_csv(csv_file, sep=';', encoding='cp1250')
        self.img_dir = img_dir
        self.tabular_cols = tabular_cols
        self.transform = transform

        # Načítanie tabuľkových dát
        self.tabular_data = self.df[tabular_cols].values.astype(np.float32)
        if scaler:
            self.tabular_data = scaler.transform(self.tabular_data)

        self.labels = self.df['intensity'].values.astype(np.float32)
        self.img_paths = self.df['image_path'].values

    def __len__(self):
        return len(self.df)

        # Načítanie snímky
    def __getitem__(self, idx):
        img_name = os.path.basename(self.img_paths[idx])
        img_path = os.path.join(self.img_dir, img_name)
        image = Image.open(img_path).convert("RGB")

        if self.transform:
            image = self.transform(image)

        return image, torch.tensor(self.tabular_data[idx]),
    torch.tensor(self.labels[idx]).unsqueeze(0)

# Trénovacia množina
train_transforms = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.RandomHorizontalFlip(),
    transforms.RandomVerticalFlip(),
    transforms.RandomRotation(30), # Zvýšená rotácia
    transforms.ColorJitter(brightness=0.2, contrast=0.2), # Zmena jasu/kontrastu
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
])

# Validačná a testovacia množina
val_transforms = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
])

# Normalizácia dát
train_df_temp = pd.read_csv(TRAIN_CSV, sep=';', encoding='cp1250')
scaler = StandardScaler()
scaler.fit(train_df_temp[TABULAR_COLUMNS].values)

# Vytvorenie Datasetov a DataLoaderov
BATCH_SIZE = 32

train_dataset = VoleDataset(TRAIN_CSV, IMG_DIR, TABULAR_COLUMNS, train_transforms, scaler)
val_dataset   = VoleDataset(VAL_CSV, IMG_DIR, TABULAR_COLUMNS, val_transforms, scaler)
test_dataset  = VoleDataset(TEST_CSV, IMG_DIR, TABULAR_COLUMNS, val_transforms, scaler)

train_loader = DataLoader(train_dataset, batch_size=BATCH_SIZE, shuffle=True)
val_loader   = DataLoader(val_dataset, batch_size=BATCH_SIZE, shuffle=False)
test_loader  = DataLoader(test_dataset, batch_size=BATCH_SIZE, shuffle=False)

class MultimodalResNet(nn.Module):
    def __init__(self, num_tabular_features):
        super(MultimodalResNet, self).__init__()

        # ResNet
        self.cnn = models.resnet18(pretrained=True)

        ct = 0
        for child in self.cnn.children():
            ct += 1
            if ct < 6:
                for param in child.parameters():
                    param.requires_grad = False

        num_cnn_features = self.cnn.fc.in_features
        self.cnn.fc = nn.Identity()

        # MLP
        self.tabular_net = nn.Sequential(
            nn.Linear(num_tabular_features, 64),
            nn.ReLU(),
            nn.Dropout(0.5),
            nn.Linear(64, 32),
            nn.ReLU()
        )

        # Fúzia
        self.final_classifier = nn.Sequential(
            nn.Linear(num_cnn_features + 32, 128),
            nn.ReLU(),
            nn.Dropout(0.5),
            nn.Linear(128, 1)
        )

    def forward(self, image, tab_data):
        x_img = self.cnn(image)
        x_tab = self.tabular_net(tab_data)
        x_combined = torch.cat((x_img, x_tab), dim=1)
        output = self.final_classifier(x_combined)
        return output

model = MultimodalResNet(num_tabular_features=len(TABULAR_COLUMNS)).to(DEVICE)

criterion = nn.BCEWithLogitsLoss() # Chybová funkcia pre binárnu klasifikáciu
optimizer = optim.Adam(model.parameters(), lr=0.00001, weight_decay=1e-4) # Optimalizátor, učí váhy
scheduler = optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', patience=1)


EPOCHS = 20          
EARLY_STOP_LIMIT = 8  

best_val_loss = float('inf')
patience_counter = 0
train_losses, val_losses = [], []
train_accs, val_accs = [], []


for epoch in range(EPOCHS):

    # Tréning
    model.train()
    running_loss = 0.0
    correct = 0
    total = 0

    # Tqdm pre grafické vykreslenie počas tréningu
    loop = tqdm(train_loader, desc=f"Epoch {epoch+1}/{EPOCHS} [Train]", leave=False)

    for images, tab_data, labels in loop:
        images, tab_data, labels = images.to(DEVICE), tab_data.to(DEVICE), labels.to(DEVICE)

        optimizer.zero_grad() # Vynulovanie gradientov
        outputs = model(images, tab_data) # Dopredný prechod
        loss = criterion(outputs, labels) # Výpočet chyby
        loss.backward() # Spätná propagácia
        optimizer.step() # Aktualizácia váh

        running_loss += loss.item()

        preds = torch.sigmoid(outputs) > 0.5
        correct += (preds == labels).sum().item()
        total += labels.size(0)

        loop.set_postfix(loss=loss.item())

    epoch_loss = running_loss / len(train_loader)
    epoch_acc = correct / total
    train_losses.append(epoch_loss)
    train_accs.append(epoch_acc)

    # Validácia
    model.eval()
    val_loss = 0.0
    val_correct = 0
    val_total = 0

    val_loop = tqdm(val_loader, desc=f"Epoch {epoch+1}/{EPOCHS} [Valid]", leave=False)

    with torch.no_grad():
        for images, tab_data, labels in val_loop:
            images, tab_data, labels = images.to(DEVICE), tab_data.to(DEVICE), labels.to(DEVICE)
            outputs = model(images, tab_data)
            loss = criterion(outputs, labels)
            val_loss += loss.item()

            preds = torch.sigmoid(outputs) > 0.5
            val_correct += (preds == labels).sum().item()
            val_total += labels.size(0)

    avg_val_loss = val_loss / len(val_loader)
    avg_val_acc = val_correct / val_total
    val_losses.append(avg_val_loss)
    val_accs.append(avg_val_acc)

    # Learning rate scheduler
    prev_lr = optimizer.param_groups[0]['lr']
    scheduler.step(avg_val_loss)
    current_lr = optimizer.param_groups[0]['lr']

    print(f"Epoch {epoch+1}/{EPOCHS} | "
          f"Train Loss: {epoch_loss:.4f} Acc: {epoch_acc:.4f} | "
          f"Val Loss: {avg_val_loss:.4f} Acc: {avg_val_acc:.4f}")

    # Logika early stopping
    if avg_val_loss < best_val_loss:
        best_val_loss = avg_val_loss
        torch.save(model.state_dict(), 'model.pth')
        print(f"Uloženie")
        patience_counter = 0
    else:
        patience_counter += 1
        print(f"Žiadne zlepšenie ({patience_counter}/{EARLY_STOP_LIMIT})")

    if patience_counter >= EARLY_STOP_LIMIT:
        print("\nZastavený tréning")
        break

    if current_lr != prev_lr:
        print(f"Znížený learning rate na {current_lr}")



# Vyhodnotenie
model.eval()
all_preds = []
all_labels = []

with torch.no_grad():
    for images, tab_data, labels in test_loader:
        images, tab_data, labels = images.to(DEVICE), tab_data.to(DEVICE), labels.to(DEVICE)
        outputs = model(images, tab_data)
        preds = torch.sigmoid(outputs) > 0.5

        all_preds.extend(preds.cpu().numpy())
        all_labels.extend(labels.cpu().numpy())

# Výpočet metrík
acc = accuracy_score(all_labels, all_preds)
prec = precision_score(all_labels, all_preds)
rec = recall_score(all_labels, all_preds)
f1 = f1_score(all_labels, all_preds)
cm = confusion_matrix(all_labels, all_preds)

print(f"Accuracy:             {acc:.4f}")
print(f"Precision:            {prec:.4f}")
print(f"Recall:               {rec:.4f}")
print(f"F1 Score:             {f1:.4f}")

